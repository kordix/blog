<h1>Przemyślenia o Ukrainie</h1>

<p style=""> Putin może być człowiekiem złym , bardzo złym, szalonym albo niesamowicie głupim - tak czy inaczej takie rzeczy trzeba
kalkulować. Jeśli Ukraina będzie się pchać do UE i NATO - będzie wojna - i Putin wielokrotnie to oznajmiał. </p>

<br>Tak więc nie powiedziałbym że to szaleństwo - raczej wypełnianie zobowiązań. To żadne usprawiedliwianie tyrana,
zaznaczam tylko że to było do przewidzenia, tak więc jeśli chcemy mieć Ukrainę w NATO czy UE trzeba było odpowiednio to
zaplanować, przyszykować odpowiednie środki albo zrezygnować z tego pomysłu, a nie dziwić się że jest wojna.


<br>Zastanawiam się gdzie ci wszyscy eksperci od pokoju z międzynarodowych organizacji, agend, think tanków? Jak nie
było od dłuższego czasu wojny to za bardzo się przyzwyczaili do wysokich gaży bez konieczności podejmowania żadnych
działań.

<br>Putin jak najbardziej ma przewidziany wariant pokojowy - byłoby to oddanie Ukrainy pod strefę wpływów Putina,
zapewne przez podstawienie promoskiewskiego pachołka.

<br>Nie podoba mi się oficjalna medialna narracja - mało konkretnych informacji , przez parę minut pokazuje się te same
urywki i tak w kółko. Zero analizy dostęnych scenariuszy. Podśmiehuja z gigantycznej jednak armii rosyjkiej - owszem,
warto wpierać morale, ale jednak. Możliwe że przekaz jest podsycany - na pewno w interesie Putina nie jest likwidowanie
jak największej ilości cywilów. W pierwszych dniach wojny pokazywano uporczywie jakiś uszkodzony blok , nie wiadomo było
czy tam trafiła rakieta czy to było siedlisko żuli. Ostatecznie się okazało że to w wyniku trafienia zestrzelonego
samolotu , a nie rakiety. Ja tu nie wspieram propagandy rosyjskiej - chodzi o to że tak to może odbierać opinia
publiczna. Tak jak z covidem - nadmięrnie rozkręcanie problemu spowodowało że ludzie zupełnie go zignorowali, nawet jak
ginęły już dziesiątki tysięcy ludzi. Ja nie chcę żeby ludzie byli obojętni na zbrodnie i krzywdę - ale takie nadmierne
podsycanie powoduje że ludzie tak właśnie reagują, a właściwie nie reagują.


<br>Bezmyślne pchanie Ukrainy do UE i NATO to nie jest dobra rzecz. Można by było na dziesiątki innych sposób wspierać
Ukrainę , a to najwyżej zostawić na sam koniec.

<br>Tak więc co można było robić już dekady temu dla Ukrainy i światowego pokoju.
<br>-korupcja. Tak - Ukraina to jeden z bardziej skorumpowanych krajów w Europie i na świecie. Ktoś mógłby im podsunąć
pożądny zestaw ustaw antykorupcyjnych czy założyć odpowiednie instytucje jak u nas Najwyższa Izba Kontroli.
<br>-edukacja - im więcej ludzi będzie miało edukację, będzie znało angielski, tym lepiej będzie sobie radziło i w
czasach pokoju, i wojny i po wojnie.
<br>-mentalność, organizacja codziennego życia, urzędów, stosunek do pracy.
<br>-całkowicie powinno się zakazać działalności wszelkiego rodzaju banderowców czy innych ludzi ze swastykami. Ja tu
nie popieram Putina, bo wiadomo że o żadną denazyfikację tu nie chodzi, ale zawsze jest to pretekst do wojny
wykorzystywany w propagandzie. Warto byłoby się odciąć od zbrodniarzy z UPA
<br>-warto byłoby zwrócić uwagę na "obszarową" biedę w Ukrainie. U nas np. ktoś zauważył że jest Polska A i Polska B.
Tam państwo funkcjonuje bardziej tak że kto się doczłapał to się doczłapał, to dosyć dziki kraj. Warto by było
wyrównywać obszarowo szanse. Tutaj mamy podział na dość bogaty wschód i maksymalnie biedny zachód , który ucieka do
Polski. Mogliby trochę sami sobie pomóc - zainwestować w infrastrukturę na zachodzie, może i kosztem wschodu, ale który
jest teraz łakomym kąskiem , efekt jest taki że wystarczy skoncentrować wojska w jednym miejscu żeby podbić to co stanowi
z 90% PKB kraju. Korupcja to też wynik zwyczajnej "kacapskiej" mentalności u zwykłych ludzi, u nas też to funkcjonuje,
ale już w dużo mniejszym stopniu. Tam w normie jest np. że się płaci za dyplom uczelni wyższych. Policjant to był
człowiek któremu trzeba dać w łapę a nie który stoi na straży prawa. Ot, tacy ludzie i taki kraj.
<br>-dobrze opłacony urzędnik - tak brzmi to kontrowersyjnie, ale to też jest potrzebne dla odpowiedniego funkcjonowania
państwa, inaczej też nie wyzbędziesz się korupcji. Tam cały naród był oburzony że polski minister dostał tyle co u nas
na kasie w Żabce.
<br>-maksymalnie powinno się też uświadamiać rosyjskie społeczeństwo, walczyć z fake newsami, propagandą. My tu sobie
pięknie opowiadamy naszą narrację, pokazujemy ofiary wojny. Ktoś zadał sobie wysiłek żeby się z tym przebić do Moskwy?
<br>-jak na mnie jak najbardziej trzeba przytaczać argumenty rosyjskiej strony, nie żeby je popierać, ale prostować.
Powiedzenie samego "fuck you, slava Ukrain" rosyjskim funkcjonariuszom dużo nie pomoże. Owszem, oni pewnie doskonale
zdają sobie sprawę, że kłamią ale są też ludzie ogłupieni i którzy po prostu zupełnie się mylą. Nieprzytaczanie tych
argumentów daje im poczucie, że oni mają prawdę objawioną, którą my chcemy ukryć. Mogłyby powstawać, książki, materiały
kontrpropagandowe , setki stron internetowych , może coś by się przebiło przez cenzurę.
<br>-teraz maksymalnie skupiamy się na pomaganiu uchodźcom bo to jest temat najbardziej medialny. Ale są też inne ważne
rzeczy - np. bronienie się przed cyberatakami. Sądzę ,że dużo ważniejsze jest wysyłanie pomocy do Ukrainy, niż
koncentrowanie się tylko na tych którym udało się tu uciec. Ci ludzie i tak stosunkowo mają szczęście, a tylko dzięki
tym którzy zostali Ukraina może się bronić. Oczywiście nie widzę nic złego w pomaganiu uchodźcom, po prostu inaczej to
kolejkuję.
<br>-bezmyślna, kretyńska, rozdmuchana walka z covidem to też przyczynek do wojny - kraje są osłabione gospodarczo, a w
ludziach wszczepiło to znieczulicę, wielu ludzi ignoruje konflikt, bo mają już dosyć po dwóch latach p.ierdolenia o
wirusie.
<br>-DYWERSYFIKACJA DOSTAW , no teraz to już każdy na to wpadł, ale trzeba było dekadę temu o tym myśleć, a nie śmiać się z Kaczyńskiego, któremu tutaj akurat przyznaję 100% racji.


