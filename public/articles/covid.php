<p >
W internecie czy gdziekolwiek w miediach cieżko znaleźć racjonalny głoś odnośnie pandemii covid. Mamy do czynienia albo z "oficjalną narracją" , która najczęściej wiąże się z gównopaniką, albo z teoriami spiskowymi lub zupełnym "olewactwem" niemałego też zagrożenia.
</p>
<br>
A więc czego jestem zwolennikiem:
<br>-szczepienia
<br>-dbanie o zdrowie, wzmacnianie odporności, suplementacja witamin itd. , zniechęcanie do palenia
<br>-unikanie większych zgromadzeń szczególnie w zamkniętych pomieszczeniach
<br>-odpowiednie restrykcje sanitarne w szpitalach

<br><br>
Czego NIE jestem zwolennikiem
<br>-paniki, chaosu, szaleństwa, przesadyzmu
<br>-teleporady
<br>-lockdownów gospodarczych, zamykania biznesów, marnowania miliardów
<br>-systemowego paralizówania służby zdrowia
<br>-mandatów za brak sprawiania żadnego zagrożenia zakażenia , braku mandatów kiedy to zagrożenie już powstaje i to ogromne , zupełnego braku konsekwencji w obostrzeniach

<br><br>

Kontakt z wirusem poza sezonem grypowym może być korzystny a nawet zbawienny, pewnie nie jednemu uratował życie. 

Weźmy na przykład pana Kowalskiego, który niespecjalnie się stosował do obostrzeń i w pierwszych miesiącach pandemii , w lato zaraził się koronawirusem. To był czas największej paniki bo to był nieznany chiński wirus, największa tragedia którą najbardziej straszyła telewizja. Ale zauważmy, że dzięki temu Kowalski zyskał przeciwciała i odporność - zupełnie inny model niż przy zakażeniach bakteriologicznych , a w sezonie pozagrypowym ryzyko zgonu również przez mniejsze obciążenie służby zdrowia było miminalne. Zauważmy też że pierwsze mutacje są swego rodzaju szczepionką do kolejnych, silniejszych. 
<br>
To teraz weźmy pana Nowaka, który maksymalnie starał się dostosować do narracji z telewizora. Przez dwa lata nie wychodził z domu, pracował wyłącznie zdalnie, a zakupy robił w Glovo. Po dwóch latach zdecydował się w końcu wyjść, bo to jednak niemożliwe żeby to trwało wiecznie, w końću to musiało nastąpić. Akurat trafił na silniejszą mutację, z czym nie poradził sobie organizm osłabiony też przez izolację. Pan Kowalski przeżył, Nowak zginął.

<br>
Wszelkiego rodzaju obostrzenia nieraz powodują gorsze gromadzenia się w skupiskach ludzi. Polecam <a target="_blank" href="https://img16.dmty.pl//uploads/202103/1616595050_2lohpm_600.jpg"> demota. </a>. "Dlaczego ludzie wierzą w to, że lockdown działa, czyli że jak zamkniesz miliony ludzi w domach i będziesz wypuszczał tylko do sklepów tworząc kolejki i skupiska to będzie lepiej, niż jak się rozjadą po kinach, hotelach, teatrach, muzeach, siłowniach i setkach innych miejsc?". Tak człowiek poszedłby sobie do restauracji gdzie jako tako utrzymuje się dystans, a tak człowiek głodny pójdzie do popularnego sklepu (który działa również jako placówka pocztowa) po hot doga - stłoczenie ludzi i wszelkiej maści zarazków wielokrotnie większe. 
<br>Rozsierdzony lud umęczony obostrzeniami gromadzi się szczególnie intensywnie kiedy przychodzi w końcu obostrzenia zdjąć, dużo intensywniej niż kiedykolwiek wcześniej gdy nie było obostrzeń. Ludziom sie wydaje ze nie ma wirusa, bo obostrzeń nie ma 
<br>Trzeba szanować ludzką cierpliwość i wytrzymałość psychiki , bo człowiek to nie robot. Jak się tego nie uszanuje to efekt jest tylko gorszy niż bez obostrzeń.

<br>W hotelach nakaz zajmowania co drugiego pokoju powodował większe gromadzenie się ludzi w tych dostępnych pokojach, podczas gdy pomieszczenia obok były puste.

<br>Mamy do czynienia albo z hichotem losu, albo cynizmem, albo niewyobrażalnym kretynizmem, może wszystkim naraz. Z jednej strony wstawiono tysiące mandatów za powodowanie zagrożenia, z drugiej w tym czasie postanowiono podjąć walkę z aborcją, akurat w najbardziej odpowiednim czasie. W każdym razie to było do przewidzenia że przy takich ustawach gromadzą się tłumy. Ja totalnie nie chcę tu mówić czy oni źle czy dobrze postępują, ani dawać tu osądów moralnych, są ważniejsze rzeczy i tyle. "Dobra, trzeba podjąć walkę z pandemią. Zbierzmy tych lewaków do kupy, wyjdzie na to że to ich wina, musi być show!".


